// Reduction Kernel — 从 Naive 到 Warp Shuffle 的优化演进
//
// 测试环境: N = 16M floats, BLOCK = 1024, GPU = RTX 4090 (sm_89)
//
// 版本对比:
//   naive_reduce  — 基准版本。步长从1开始倍增 (s<<=1)，相邻线程间隔跳跃访问。
//                   问题：tid % (s*2)==0 导致严重 warp divergence，大量线程空转。
//                   bank conflict: 访问模式导致 shared memory 冲突。
//
//   reduce_v0     — 修复 warp divergence。改为从高位往低位折叠 (s>>=1)，
//                   活跃线程连续排列，消除 divergence。
//                   但 bank conflict 仍然存在（步长为1时相邻地址冲突）。
//
//   reduce_v1     — 修复 bank conflict。反转折叠方向，从 blockDim/2 往下折叠，
//                   访问地址 sdata[tid] 与 sdata[tid+s] 间距 >= 32，无冲突。
//                   这是教科书标准版本。
//
//   reduce_v2     — 减少 idle thread。每个 block 在 load 阶段处理 2 倍数据
//                   (gid 和 gid+blockDim.x)，grid 数量减半，GPU 利用率提升。
//
//   reduce_v3     — unroll 最后一个 warp。当 s<=32 时，同一 warp 内天然同步，
//                   不需要 __syncthreads()。手动展开 6 步并用 volatile 防止
//                   编译器优化掉内存写入，减少同步开销。
//
//   reduce_v4     — 用 __syncwarp() 替换 volatile。比 volatile 语义更精确，
//                   明确表达 warp 内同步意图，对编译器更友好。
//
//   reduce_v5     — 完全消除 shared memory，改用 warp shuffle。
//                   __shfl_down_sync 直接在寄存器之间传递数据，
//                   每个 warp 内 reduce 后写入 smem[warp_id]，
//                   最后一个 warp 再做一次 shuffle reduce 得到最终结果。
//                   无 bank conflict，延迟最低。
// =============================================================================


