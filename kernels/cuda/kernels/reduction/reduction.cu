__global__ void reduce_v5(const float *__restrict__ in, float *__restrict__ out, int n)
{
    extern __shared__ float sdddata[32];
    int tid = threadIdx.x;
    int gid = blockIdx.x * (blockDim.x * 2) + threadIdx.x;

    float mySum = 0.0f;
    if (gid < n) {
        mySum += in[gid];
    }
    if (gid + blockDim.x < n) {
        mySum += in[gid + blockDim.x];
    }
    __syncthreads();

    mySum += __shfl_down_sync(0xffffffff, mySum, 16);
    mySum += __shfl_down_sync(0xffffffff, mySum, 8);
    mySum += __shfl_down_sync(0xffffffff, mySum, 4);
    mySum += __shfl_down_sync(0xffffffff, mySum, 2);
    mySum += __shfl_down_sync(0xffffffff, mySum, 1);

    int lanbool = tid % 32;
    int lanid = tid / 32;

    if(lanbool == 0)
    {
        sdddata[lanid] = mySum;
    }
    __syncthreads();
    if(tid < 32) {
        mySum = (tid < 32) ? sdddata[lanid] : 0;
        mySum += __shfl_down_sync(0xffffffff, mySum, 16);
        mySum += __shfl_down_sync(0xffffffff, mySum, 8);
        mySum += __shfl_down_sync(0xffffffff, mySum, 4);
        mySum += __shfl_down_sync(0xffffffff, mySum, 2);
        mySum += __shfl_down_sync(0xffffffff, mySum, 1);
    }


    if (tid == 0)
    {
        out[blockIdx.x] = mySum;
    }
}

__global__ void naive_reduce(const float* __restrict__ in, float* __restrict__ out, int n)
{
    extern __shared__ float sdata[];
    int tid = threadIdx.x;
    int gid = blockIdx.x * blockDim.x + threadIdx.x;

    sdata[tid] = (gid < n) ? in[gid] : 0;
    __syncthreads();
    for(int s = 1; s < blockDim.x; s <<= 1)
    {
        if(tid % (s * 2) == 0)
            sdata[tid] += sdata[tid + s];
        __syncthreads();    
    }
    if(threadIdx.x == 0)
        out[blockIdx.x] = sdata[tid];
}

