    sdata[tid] = (gid < n) ? in[gid] : 0;
    __syncthreads();

    for(int s = 1; s < blockDim.x;s <<= 1)
    {
        if(tid * s * 2 < blockDim.x)
        {
            
            sdata[tid * s * 2] += sdata[tid * s * 2 + s];
